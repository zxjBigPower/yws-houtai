        <div class="con-l fl"><h3>全国上半年跨境电子商务交易2.6万亿</h3>
            <p>2016年10月14日 11时15分</p>
            <div class="con-l-box"><p>&nbsp;&nbsp;&nbsp;&nbsp;第十三届中国国际中小企业博览会(下称“中博会”)正在如火如荼进行中。昨日上午，“中博会”五大展之一的跨境电商展暨2016中国跨境电商领袖峰会在广州琶洲保利世贸博览馆举行。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;广东省人民政府副秘书长李贻伟、工信部中小企业局局长马向晖、广东省商务厅巡视员罗练锦、广东省经济和信息化委员会副主任林位超等领导出席该次活动，全国跨境电商领域杰出企业的主要负责人、专家学者以及来自俄罗斯、瑞士、韩国电商企业等七百余位跨境电商行业精英参加了本次峰会。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;记者从论坛上获悉，近年来，我国跨境电子商务异军突起、蓬勃发展，2015年跨境电子商务交易规模达到5.4万亿元，同比增长28.6%；2016年上半年跨境电子商务交易规模为2.6万亿元，同比增长30%。跨境电子商务作为新兴商业模式，是稳定外贸增长的潜在动力，是创新驱动发展的重要引擎，已成为我国最有机会超越欧美国家的战略性产业。</p>
                <p>
                    &nbsp;&nbsp;&nbsp;&nbsp;中国电子商务有望成为引领世界经济发展的新引擎。广东省作为全国第一外贸大省，站在了跨境电子商务发展高地的最前沿，跨境电子商务交易额占全国交易总额的七成。</p>
